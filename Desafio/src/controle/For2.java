package controle;

public class For2 {
    public static void main(String[] args) {

        //for decrementando -2
        for(int i = 10; i>=0; i-=2){
            System.out.println(i);

            for (int j = 0; j <10; j++){
                System.out.println(j);
            }
        }


    }
}
